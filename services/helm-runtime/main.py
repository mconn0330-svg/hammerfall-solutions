@app.post("/invoke/{agent_role}", response_model=InvokeResponse)
async def invoke(
    agent_role: str = FastAPIPath(..., description="Agent role to invoke"),
    body: InvokeRequestBody = ...,
) -> InvokeResponse:
    """
    Route a request to the model configured for agent_role.
    Runs the full middleware pipeline (pre → handler → post).
    """
    # Confirm the role is configured
    try:
        router.get_agent_config(agent_role)
    except UnknownRoleError as e:
        raise HTTPException(status_code=404, detail={"error": "unknown_role", "detail": str(e)})

    # Confirm we have a handler
    handler = AGENT_HANDLERS.get(agent_role)
    if not handler:
        raise HTTPException(
            status_code=404,
            detail={"error": "unknown_role", "detail": f"No handler for role: {agent_role}"},
        )

    # Build internal request object
    req = InvokeRequest(
        session_id=body.session_id,
        turn_number=body.turn_number,
        user_message=body.user_message,
        helm_response=body.helm_response,
        context=body.context,
    )

    # Pre-model middleware → agent handler → post-model middleware
    # Single outer try/except ensures PrimeDirectivesViolation is caught
    # regardless of which pipeline stage raises it. Pre-model was previously
    # unguarded — a guard trip there would have returned HTTP 500 instead of 403.
    try:
        req = pipeline.run_pre(agent_role, req)
        output = await handler(req)
        output = pipeline.run_post(agent_role, output)
    except PrimeDirectivesViolation as e:
        logger.warning(
            "Prime Directive violation blocked request: %s — %s", e.directive, e.detail
        )
        return JSONResponse(
            status_code=403,
            content={
                "error": "prime_directive_violation",
                "directive": e.directive,
                "detail": e.detail,
            },
        )
    except ValueError as e:
        # Validation failures from output_validator (malformed Projectionist frame)
        raise HTTPException(
            status_code=422,
            detail={"error": "validation_failed", "detail": str(e)},
        )
    except Exception as e:
        logger.exception("Agent handler error for role '%s': %s", agent_role, e)
        raise HTTPException(
            status_code=500,
            detail={"error": "handler_error", "detail": str(e)},
        )

    return InvokeResponse(
        role=agent_role,
        output=output,
        session_id=body.session_id,
        turn_number=body.turn_number,
    )


@app.get("/health")
async def health() -> JSONResponse:
    """
    Service health check. Checks three things:
      1. Runtime service is up (trivially true if this endpoint responds)
      2. All configured model endpoints are reachable
      3. Supabase REST API is reachable and helm_frames is queryable

    Returns HTTP 200 in all cases — status field indicates healthy/degraded.
    HTTP 500 is reserved for the health endpoint itself failing (runtime crash).
    """
    checks: dict[str, Any] = {"service": "ok", "models": {}, "supabase": {}}
    overall = "healthy"

    # Check each configured model
    for role in ("projectionist", "archivist", "helm_prime", "speaker", "contemplator"):
        try:
            result = await router.check_model_health(role)
            checks["models"][role] = result
            if result["status"] != "ok":
                overall = "degraded"
        except Exception as e:
            checks["models"][role] = {"status": "error", "error": str(e)}
            overall = "degraded"

    # Check Supabase
    sb_ok = await supabase.health_check()
    if sb_ok:
        checks["supabase"] = {"status": "ok", "table": "helm_frames", "rows_queryable": True}
    else:
        checks["supabase"] = {
            "status": "unreachable",
            "error": "Could not query helm_frames table",
        }
        overall = "degraded"

    return JSONResponse(content={"status": overall, "checks": checks})


@app.get("/config/agents")
async def config_agents() -> JSONResponse:
    """
    Return the current agent-to-model mapping.
    No secrets exposed — provider type, model name, and base URL only.
    API keys are intentionally omitted.
    """
    return JSONResponse(content={"agents": router.config_summary()})
