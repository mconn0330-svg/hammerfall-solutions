"""
main.py — Helm Runtime Service

FastAPI service that routes agent role requests to the correct model backend
via LiteLLM. Provider-agnostic. All model and provider configuration lives in
config.yaml. No hardcoded URLs, keys, or model names in this file.

Endpoints:
  POST /invoke/{agent_role}  — Route request to configured model, return response
  GET  /health               — Service up + all model endpoints + Supabase reachable
  GET  /config/agents        — Current agent-to-model mapping (no secrets)

BA7a: Service skeleton with stubbed agent handlers.
      Routing, middleware pipeline, health check, and config endpoint are active.
      Actual model calls to Projectionist/Archivist are wired in BA7b/BA7c.
"""

import logging
from contextlib import asynccontextmanager
from pathlib import Path
from typing import Any

from fastapi import FastAPI, HTTPException, Path as FastAPIPath
from fastapi.responses import JSONResponse
from pydantic import BaseModel, Field

from agents import archivist as archivist_agent
from agents import contemplator as contemplator_agent
from agents import projectionist as projectionist_agent
from agents import speaker as speaker_agent
from embedding_client import EmbeddingClient
from middleware import InvokeRequest, MiddlewarePipeline, PrimeDirectivesViolation
from model_router import ConfigError, ModelRouter, UnknownRoleError
from supabase_client import SupabaseClient

# ---------------------------------------------------------------------------
# Logging
# ---------------------------------------------------------------------------

logging.basicConfig(
    level=logging.INFO,
    format="%(asctime)s [%(levelname)s] %(name)s: %(message)s",
)
logger = logging.getLogger(__name__)

# ---------------------------------------------------------------------------
# Service globals — initialized at startup
# ---------------------------------------------------------------------------

CONFIG_PATH = Path(__file__).parent / "config.yaml"

router: ModelRouter = None
supabase: SupabaseClient = None
pipeline: MiddlewarePipeline = None
embedding_client: EmbeddingClient = None


@asynccontextmanager
async def lifespan(app: FastAPI):
    """Initialize service globals at startup. Fail fast on config errors."""
    global router, supabase, pipeline, embedding_client

    logger.info("Helm Runtime Service starting...")

    try:
        router = ModelRouter(str(CONFIG_PATH))
    except ConfigError as e:
        logger.critical("Configuration error at startup: %s", e)
        raise SystemExit(1) from e

    supabase = SupabaseClient(
        url=router.supabase_url,
        service_key=router.supabase_service_key,
    )

    if router.embedding_api_key:
        embedding_client = EmbeddingClient(
            api_key=router.embedding_api_key,
            model=router.embedding_model,
        )
        logger.info("EmbeddingClient initialized. model=%s", router.embedding_model)
    else:
        logger.warning(
            "Embeddings not configured — Archivist writes will not include embeddings. "
            "Add embeddings block to config.yaml to enable semantic search."
        )

    pipeline = MiddlewarePipeline()

    logger.info("Helm Runtime Service ready.")
    yield

    logger.info("Helm Runtime Service shutting down.")


# ---------------------------------------------------------------------------
# FastAPI app
# ---------------------------------------------------------------------------

app = FastAPI(
    title="Helm Runtime Service",
    description=(
        "Provider-agnostic agent orchestration layer. "
        "Routes Helm agent role requests to the correct model backend."
    ),
    version="0.1.0",
    lifespan=lifespan,
)


# ---------------------------------------------------------------------------
# Request / Response models
# ---------------------------------------------------------------------------

class InvokeRequestBody(BaseModel):
    session_id: str = Field(..., description="UUID generated by Claude Code at session start")
    turn_number: int = Field(..., ge=0, description="Turn counter from TURN_COUNT")
    user_message: str = Field(..., description="Verbatim user message — no truncation")
    helm_response: str = Field(..., description="Verbatim Helm Prime response — no truncation")
    context: dict = Field(
        default_factory=lambda: {"project": "hammerfall-solutions", "agent": "helm"},
        description="Project and agent scope for brain writes",
    )


class InvokeResponse(BaseModel):
    role: str
    output: str
    session_id: str
    turn_number: int


# ---------------------------------------------------------------------------
# Agent handlers — BA7a stubs
# BA7b wires Projectionist. BA7c wires Archivist.
# ---------------------------------------------------------------------------

async def _handle_projectionist(req: InvokeRequest) -> str:
    """Route to Projectionist handler — frame creation via qwen3:4b on Ollama."""
    return await projectionist_agent.handle(req, router, supabase)


async def _handle_archivist(req: InvokeRequest) -> str:
    """Route to Archivist handler — frame migration with embedding via qwen3:14b on Ollama."""
    return await archivist_agent.handle(req, router, supabase, embedding_client)


async def _handle_contemplator(req: InvokeRequest) -> str:
    """
    Contemplator — Helm's inner life. S1-BA3.

    session_start: Pass 1 only, think=False (~10s, non-blocking).
                   Returns curiosity flags for Helm Prime Routine 0.
    session_end:   Pass 1 + Pass 2, think=True (~30s, deep synthesis).
                   Returns full write payload, then triggers Archivist
                   write handoff (belief patches, patterns, flags, reflection).

    Trigger is read from req.context["trigger"]. Defaults to "session_end".
    """
    import json as _json

    result_str = await contemplator_agent.handle(req, router, supabase)
    result = _json.loads(result_str)

    # session_end: hand off write payload to Archivist
    if result.get("trigger") == "session_end" and result.get("status") == "session_end_complete":
        payload = result.get("payload", {})
        if payload:
            write_req = InvokeRequest(
                session_id=req.session_id,
                turn_number=req.turn_number,
                user_message=req.user_message,
                helm_response=req.helm_response,
                context={**req.context, "contemplator_writes": payload},
            )
            await archivist_agent.handle(write_req, router, supabase, embedding_client)
            logger.info(
                "Contemplator→Archivist write handoff complete. session=%s", req.session_id
            )

    return result_str


async def _handle_speaker(req: InvokeRequest) -> str:
    """
    Speaker routing — S1-BA1.
    Classifies request as simple (resolve locally via Qwen2.5 3B) or complex
    (escalate to Helm Prime). Classification failure defaults to Helm Prime.
    See agents/speaker.py for full implementation.
    """
    return await speaker_agent.handle(req, router, supabase)


async def _handle_helm_prime(req: InvokeRequest) -> str:
    """
    Helm Prime runs on Claude Code, not the runtime. This endpoint exists for
    completeness and future use (e.g. Hammerfall Cloud routing Helm Prime calls).
    """
    return f"[STUB] Helm Prime invocation via runtime. Future use."


AGENT_HANDLERS = {
    "projectionist": _handle_projectionist,
    "archivist": _handle_archivist,
    "speaker": _handle_speaker,
    "helm_prime": _handle_helm_prime,
    "contemplator": _handle_contemplator,
}


